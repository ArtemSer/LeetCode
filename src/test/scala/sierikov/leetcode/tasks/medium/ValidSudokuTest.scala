package sierikov.leetcode.tasks.medium

import org.scalatest.flatspec.AnyFlatSpec

class ValidSudokuTest extends AnyFlatSpec {
  import ValidSudoku._

  it should "detect valid sudoku" in {
    val board = Array(
      Array('5', '3', '.', '.', '7', '.', '.', '.', '.'),
      Array('6', '.', '.', '1', '9', '5', '.', '.', '.'),
      Array('.', '9', '8', '.', '.', '.', '.', '6', '.'),
      Array('8', '.', '.', '.', '6', '.', '.', '.', '3'),
      Array('4', '.', '.', '8', '.', '3', '.', '.', '1'),
      Array('7', '.', '.', '.', '2', '.', '.', '.', '6'),
      Array('.', '6', '.', '.', '.', '.', '2', '8', '.'),
      Array('.', '.', '.', '4', '1', '9', '.', '.', '5'),
      Array('.', '.', '.', '.', '8', '.', '.', '7', '9')
    )
    assert(isValidSudoku(board))
  }

  it should "detect invalid sudoku" in {
    val board = Array(
      Array('8', '3', '.', '.', '7', '.', '.', '.', '.'),
      Array('6', '.', '.', '1', '9', '5', '.', '.', '.'),
      Array('.', '9', '8', '.', '.', '.', '.', '6', '.'),
      Array('8', '.', '.', '.', '6', '.', '.', '.', '3'),
      Array('4', '.', '.', '8', '.', '3', '.', '.', '1'),
      Array('7', '.', '.', '.', '2', '.', '.', '.', '6'),
      Array('.', '6', '.', '.', '.', '.', '2', '8', '.'),
      Array('.', '.', '.', '4', '1', '9', '.', '.', '5'),
      Array('.', '.', '.', '.', '8', '.', '.', '7', '9')
    )
    assert(!isValidSudoku(board))

    val board1 = Array(
      Array('.', '.', '4', '.', '.', '.', '6', '3', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('5', '.', '.', '.', '.', '.', '.', '9', '.'),
      Array('.', '.', '.', '5', '6', '.', '.', '.', '.'),
      Array('4', '.', '3', '.', '.', '.', '.', '.', '1'),
      Array('.', '.', '.', '7', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '5', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.')
    )
    assert(!isValidSudoku(board1))

    val empty = Array(
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.'),
      Array('.', '.', '.', '.', '.', '.', '.', '.', '.')
    )
    assert(isValidSudoku(empty))
  }

}
